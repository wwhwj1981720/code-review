package unicom;
/**
 * 定义存放关键字的类
 * @author lenovo
 *
 */
public class WordUnit {
	String value;
	int ID;
	int row;
	public int getID() {
		return ID;
	}
	public void setID(int id) {
		ID = id;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public int getRow() {
		return row;
	}
	public void setRow(int row) {
		this.row = row;
	}	
}
