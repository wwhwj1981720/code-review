package unicom;

public class CountTotal {
	long totalrow;
	long totalvalrow;
	public long getTotalrow() {
		return totalrow;
	}
	public void setTotalrow(long totalrow) {
		this.totalrow = totalrow;
	}
	public long getTotalvalrow() {
		return totalvalrow;
	}
	public void setTotalvalrow(long totalvalrow) {
		this.totalvalrow = totalvalrow;
	}
	public CountTotal() {
		super();
		totalrow=0;
		totalvalrow=0;
	}
	
}
